CREATE TABLE IF NOT EXISTS t_property (
	id INT(11) NOT NULL AUTO_INCREMENT,
	nameAR VARCHAR(50) DEFAULT NULL,
	nameFR VARCHAR(50) DEFAULT NULL,
	nameES VARCHAR(50) DEFAULT NULL,
	nameDE VARCHAR(50) DEFAULT NULL,
	nameNL VARCHAR(50) DEFAULT NULL,
	descriptionAR TEXT DEFAULT NULL,
	descriptionFR TEXT DEFAULT NULL,
	descriptionES TEXT DEFAULT NULL,
	descriptionDE TEXT DEFAULT NULL,
	descriptionNL TEXT DEFAULT NULL,
	price DECIMAL(12,2) DEFAULT NULL,
	address TEXT DEFAULT NULL,
	phone VARCHAR(50) DEFAULT NULL,
	type VARCHAR(50) DEFAULT NULL,
	img1 VARCHAR(255) DEFAULT NULL,
	img2 VARCHAR(255) DEFAULT NULL,
	img3 VARCHAR(255) DEFAULT NULL,
	img4 VARCHAR(50) DEFAULT NULL,
	created DATETIME DEFAULT NULL,
	createdBy VARCHAR(50) DEFAULT NULL,
	updated DATETIME DEFAULT NULL,
	updatedBy VARCHAR(50) DEFAULT NULL,
	PRIMARY KEY (id)
) ENGINE=InnoDB DEFAULT CHARSET=utf8 AUTO_INCREMENT=1 ;